%  MATLAB script to calculate the cost of a trip based on the mode of transportation. Use a switch statement to handle different case
Mode = 'airplane';
switch Mode 
     case 'Car'
         disp('The cost will be 10 l.E per mile');
     case 'Train'
        disp('The cost will be 15 l.E per mile');
     case 'Bus'
        disp('The cost will be 20 l.E per mile');
     case 'Airplane'
        disp('The cost will be 100 l.E per mile');
end              